package fr.tiogars.network;

/**
 * Enumeration representing the possible types of devices in a network.
 * Each type helps categorize the device for better network management and organization.
 */
public enum DeviceType {
    /** Server device type. */
    Server,
    /** Router device type. */
    Router,
    /** Firewall device type. */
    Firewall,
    /** Switch device type. */
    Switch,
    /** Access Point device type. */
    AccessPoint,
    /** Repeater device type. */
    Repeater,
    /** Printer device type. */
    Printer,
    /** Desktop device type. */
    Desktop,
    /** Laptop device type. */
    Laptop,
    /** Tab device type. */
    Tab,
    /** Phone device type. */
    Phone,
    /** Television device type. */
    Television
}
